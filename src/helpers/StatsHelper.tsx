export function getSportStats(sport: string) {
  //console.log('getSportStats', sport, normalizeSportGender(sport));
  switch (normalizeSportGender(sport)) {
    case 'football':
      return Football;
    case 'lacrosse':
      return Lacrosse;
    case 'softball':
      return Softball;
    case 'soccer':
      return Soccer;
    case 'baseball':
      return Baseball;
    case 'basketball':
      return Basketball;
    case 'volleyball':
      return Volleyball;
    case '':
      return {};
    default:
      console.error('Unknown sport:', normalizeSportGender(sport), sport);
      return {};
  }
}
